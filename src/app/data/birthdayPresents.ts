// 最新のプレゼントを取得する関数
export const getLatestPresent = (): BirthdayPresent => {
  // 日付でソートして最新のものを返す
  return [...birthdayPresents].sort((a, b) => {
    return new Date(b.date).getTime() - new Date(a.date).getTime();
  })[0];
};

// すべてのプレゼントを取得する関数
export const getAllPresents = (): BirthdayPresent[] => {
  return [...birthdayPresents].sort((a, b) => {
    return new Date(b.date).getTime() - new Date(a.date).getTime();
  });
};

